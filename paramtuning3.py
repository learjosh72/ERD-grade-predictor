def softmax(x):
    e = np.exp(x - np.max(x))
    return e / e.sum()

def sample_simplex(trial, n, name):
    raw = np.array([trial.suggest_float(f"{name}_{i}", -4, 4) for i in range(n)])
    return softmax(raw)

